/* 1.3 Erkläre warum OOP für große System sinnvoll ist.
OOP ermöglicht die Kapselung von Daten und Funktionen in Klassen, was die Wartbarkeit und Wiederverwendbarkeit von Code verbessert.
Es erleichtert die Zusammenarbeit in Teams, da verschiedene Entwickler an verschiedenen Klassen arbeiten können, ohne sich gegenseitig zu stören. */

